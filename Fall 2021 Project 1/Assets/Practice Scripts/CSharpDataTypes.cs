using System;

namespace Helloworld
{
    class Program
    {
        static void Main(string[] args)
        {
            int myNum = 5; //integers are whole numbers
            double myDoubleNum = 5.99D; //floating point number. Note: D after number
            char myLetter = 'D'; //Character. Note: ''
            bool myBool = true; //true or false only
            string myText = "HI!"; //multiple letters or words
            Console.WriteLine(myNum);
            Console.WriteLine(myDoubleNum);
            Console.WriteLine(myLetter);
            Console.WriteLine(myBool);
            Console.WriteLine(myText);
        }
    }
}